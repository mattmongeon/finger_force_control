#include "stopwatch.h"
#include "utils.h"


////////////////////////////////////////////////////////////////////////////////
//  Construction / Destruction
////////////////////////////////////////////////////////////////////////////////

cStopwatch::cStopwatch()
{

}

////////////////////////////////////////////////////////////////////////////////
//  Interface Functions
////////////////////////////////////////////////////////////////////////////////

void cStopwatch::Start()
{
	if( mTimer.is_stopped() )
		mTimer.start();
}

////////////////////////////////////////////////////////////////////////////////

void cStopwatch::Stop()
{
	mTimer.stop();
}

////////////////////////////////////////////////////////////////////////////////

double cStopwatch::GetElapsedTime_ms() const
{
	return mTimer.elapsed().wall / 1000000.0;
}

